fun main() {
    //arithmetic operators
    var num1 = 45
    var num2 = 34
    println(num1+num2)
    println(num1-num2)
    println(num1/num2)
    println(num1*num2)
    println(num1%num2)


    //comparison operators
    println(num1>num2)
    println(num1<num2)
    println(num1>=num2)
    println(num1<=num2)
    println(num1==num2)
    println(num1!=num2)

    //logic operators
    println(num1<num2 && num1!=num2)
    println(num1<num2 || num1!=num2)
    println(!(num1<num2 || num1!=num2))

    //assignment operator
    var x = 23
    x += 2
    println(x)

    var y = 45
    y %= 2
    println(y)


















}